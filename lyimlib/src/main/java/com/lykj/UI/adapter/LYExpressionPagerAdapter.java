package com.lykj.UI.adapter;

import java.util.ArrayList;

import com.lykj.emojicon.EmojiconGridFragment;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;

public class LYExpressionPagerAdapter extends FragmentStatePagerAdapter {

	private ArrayList<EmojiconGridFragment> fragments;

    public LYExpressionPagerAdapter(FragmentManager fm, ArrayList<EmojiconGridFragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int i) {
        return fragments.get(i);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }

}
